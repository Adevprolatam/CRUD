    private void jButtonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearActionPerformed
  // Obtener la informaci√≥n de los campos de texto
    int ci = Integer.parseInt(jTextFieldCi.getText()); // Reemplaza jTextFieldCI con el nombre real de tu campo
    String nombre = jTextFieldNombre.getText();
    String apellido = jTextFieldApellido.getText();
    String email = jTextFieldEmail.getText();
    int edad = Integer.parseInt(jTextFieldEdad.getText());
    String genero = (String) jComboBoxGenero.getSelectedItem();

    // Crear un objeto Usuario con la informaci√≥n ingresada
    Usuario user = new Usuario(ci, nombre, apellido, email, edad, genero);

    // Puedes hacer lo que necesites con el objeto Usuario aqu√≠
    Usuario.agregarUsuario(user);
    // Por ejemplo, almacenarlo en la base de datos usando UsuarioDAO

    // Tambi√©n puedes mostrar un mensaje o realizar otras acciones despu√©s de crear el usuario
    JOptionPane.showMessageDialog(this, "Usuario creado con √©xito");

    // Limpia los campos despu√©s de crear el usuario (opcional)
    jTextFieldCi.setText("");
    jTextFieldNombre.setText("");
    jTextFieldApellido.setText("");
    jTextFieldEmail.setText("");
    jTextFieldEdad.setText("");
    jComboBoxGenero.setSelectedIndex(0);
    }//GEN-LAST:event_jButtonCrearActionPerformed

