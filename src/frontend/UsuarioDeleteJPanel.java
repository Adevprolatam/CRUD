    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
       try {
    // Obtener el CI del campo de texto
    int ci = Integer.parseInt(jTextFieldCi.getText());

    // Eliminar el usuario por CI
    Usuario.eliminarUsuario(String.valueOf(ci));

    // Mostrar mensaje de √©xito
    JOptionPane.showMessageDialog(this, "Usuario eliminado de la DB con √©xito");

    // Limpiar los campos y labels
    jTextFieldCi.setText("");
    jLabelCI.setText("s/n");
    jLabelNombre.setText("s/n");
    jLabelApellido.setText("s/n");
} catch (NumberFormatException e) {
    // Manejar la excepci√≥n si el CI no es un n√∫mero v√°lido
    JOptionPane.showMessageDialog(this, "Ingrese un CI v√°lido");
}
    }//GEN-LAST:event_jButtonEliminarActionPerformed

    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
      try {
        // Obtener el CI del campo de texto
        int ci = Integer.parseInt(jTextFieldCi.getText());

        // Buscar el usuario por CI
        Usuario usuario = Usuario.getUserById(String.valueOf(ci));

        // Mostrar la informaci√≥n en los labels
        if (usuario != null) {
            jLabelCI.setText(String.valueOf(usuario.getCi()));
            jLabelNombre.setText(usuario.getNombre());
            jLabelApellido.setText(usuario.getApellido());
        } else {
            // Si el usuario no existe, mostrar un mensaje
            JOptionPane.showMessageDialog(this, "Usuario no encontrado");
            jLabelCI.setText("s/n");
            jLabelNombre.setText("s/n");
            jLabelApellido.setText("s/n");
        }
    } catch (NumberFormatException e) {
        // Manejar la excepci√≥n si el CI no es un n√∫mero v√°lido
        JOptionPane.showMessageDialog(this, "Ingrese un CI v√°lido");
    }       
      
    }//GEN-LAST:event_jButtonBuscarActionPerformed


